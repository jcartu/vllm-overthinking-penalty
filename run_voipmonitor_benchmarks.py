#!/usr/bin/env python3
"""
run_voipmonitor_benchmarks.py — Orchestrates the ESTONIA and LAVD tests from 
the voipmonitor llm-inference-bench repository. Runs them for both Plugin ON and 
Plugin OFF states, parses outputs, compiles comparison tables, updates README, and pushes to GitHub.
"""

import json
import os
import subprocess
import sys
import time

CONFIG_PATH = "/data1/vllm-plugins/dynamic_config.json"
BENCH_DIR = "/home/josh/llm-inference-bench"
OUTPUT_DIR = "/data1/vllm-plugins"
README_PATH = "/data1/vllm-plugins/README.md"

def set_lambda(val: float):
    print(f"\n[CONFIG] Setting Overthinking Lambda to {val:.1f}...", flush=True)
    with open(CONFIG_PATH, "w") as f:
        json.dump({"lambda": val}, f)
    # Wait past the worker TTL cache threshold
    time.sleep(1.5)

def run_test(profile: str, mode: str, output_file: str):
    print(f"\n[EXEC] Running {profile.upper()} test in {mode.upper()} mode...", flush=True)
    cmd = [
        "python3", "llm_decode_bench.py",
        "--port", "8000",
        "--model", "glm-5.2",
        "--test-profile", profile,
        "--profile-concurrency", "4",
        "--profile-runs", "10",
        "--max-tokens", "2048",
        "--display-mode", "plain",
        "--output", output_file
    ]
    try:
        subprocess.run(cmd, cwd=BENCH_DIR, check=True)
        print(f"[SUCCESS] Saved results to {output_file}", flush=True)
    except subprocess.CalledProcessError as e:
        print(f"[ERROR] Test failed for {profile} ({mode}): {e}", flush=True)
        sys.exit(1)

def extract_summary_metrics(file_path: str) -> dict:
    """Read the JSON result generated by llm_decode_bench.py and extract key stats."""
    if not os.path.exists(file_path):
        return {}
    with open(file_path, "r") as f:
        data = json.load(f)
    
    summary = data.get("selected_summary") or {}
    if not summary:
        return {"throughput": 0.0, "avg_tokens": 0.0, "correctness": 0.0, "ttft": 0.0}
        
    return {
        "throughput": float(summary.get("aggregate_gen_tok_s") or 0.0),
        "avg_tokens": float(summary.get("completion_tokens", {}).get("avg") or 0.0),
        # correctness rate is already a percentage in selected_summary
        "correctness": float(summary.get("correct_rate") or 0.0),
        "ttft": float(summary.get("ttft", {}).get("avg") or 0.0),
    }

def main():
    print("==========================================================", flush=True)
    print("  VOIPMONITOR BENCHMARK ORCHESTRATOR: ESTONIA & LAVD  ", flush=True)
    print("==========================================================", flush=True)
    
    hotel_lights_off = f"{OUTPUT_DIR}/hotel_lights_off.json"
    hotel_lights_on = f"{OUTPUT_DIR}/hotel_lights_on.json"
    lavd_off = f"{OUTPUT_DIR}/lavd_off.json"
    lavd_on = f"{OUTPUT_DIR}/lavd_on.json"
    
    # 1. Run Baseline (Plugin OFF, lambda=0.0)
    set_lambda(0.0)
    run_test("hotel-lights", "off", hotel_lights_off)
    run_test("lavd-test", "off", lavd_off)
    
    # 2. Run Mitigated (Plugin ON, lambda=5.0)
    set_lambda(5.0)
    run_test("hotel-lights", "on", hotel_lights_on)
    run_test("lavd-test", "on", lavd_on)
    # 3. Extract and compile
    print("\n[PROCESS] Compiling results...", flush=True)
    
    h_off = extract_summary_metrics(hotel_lights_off)
    h_on = extract_summary_metrics(hotel_lights_on)
    l_off = extract_summary_metrics(lavd_off)
    l_on = extract_summary_metrics(lavd_on)
    
    # Generate Markdown section
    table_md = f"""
## 5. VoIPmonitor Official Benchmark Results (LAVD & HOTEL-LIGHTS)

These benchmarks are sourced from Martin Vit's official voipmonitor `llm-inference-bench` repository. They measure the exact same GLM-5.2 engine under sustained concurrency ($C=4$, $N=10$ trials) with the overthinking penalty turned **ON** ($\\lambda = 5.0$) vs **OFF** ($\\lambda = 0.0$).

### A. HOTEL-LIGHTS Reasoning Consistency Test
*A compact reasoning test with an expected numeric answer of 48. It checks whether the model handles repeated toggles plus the cat reset rule.*

| Metric | Plugin OFF ($\\lambda = 0.0$) | Plugin ON ($\\lambda = 5.0$) | Difference |
| :--- | :---: | :---: | :---: |
| **Decode Throughput** | {h_off.get('throughput', 0.0):.2f} tok/s | {h_on.get('throughput', 0.0):.2f} tok/s | **{(((h_on.get('throughput', 0.0) - h_off.get('throughput', 0.0)) / max(0.1, h_off.get('throughput', 0.0))) * 100):+.1f}%** |
| **Avg Completion Tokens** | {h_off.get('avg_tokens', 0.0):.1f} | {h_on.get('avg_tokens', 0.0):.1f} | **{(((h_on.get('avg_tokens', 0.0) - h_off.get('avg_tokens', 0.0)) / max(0.1, h_off.get('avg_tokens', 0.0))) * 100):+.1f}%** |
| **Correctness Rate** | {h_off.get('correctness', 0.0):.1f}% | {h_on.get('correctness', 0.0):.1f}% | **{ (h_on.get('correctness', 0.0) - h_off.get('correctness', 0.0)):+.1f}%** |
| **Avg TTFT (s)** | {h_off.get('ttft', 0.0):.3f}s | {h_on.get('ttft', 0.0):.3f}s | **{(((h_on.get('ttft', 0.0) - h_off.get('ttft', 0.0)) / max(0.001, h_off.get('ttft', 0.0))) * 100):+.1f}%** |

### B. LAVD Context Consistency Test
*The LAVD arithmetic and context retention test profile.*

| Metric | Plugin OFF ($\\lambda = 0.0$) | Plugin ON ($\\lambda = 5.0$) | Difference |
| :--- | :---: | :---: | :---: |
| **Decode Throughput** | {l_off.get('throughput', 0.0):.2f} tok/s | {l_on.get('throughput', 0.0):.2f} tok/s | **{(((l_on.get('throughput', 0.0) - l_off.get('throughput', 0.0)) / max(0.1, l_off.get('throughput', 0.0))) * 100):+.1f}%** |
| **Avg Completion Tokens** | {l_off.get('avg_tokens', 0.0):.1f} | {l_on.get('avg_tokens', 0.0):.1f} | **{(((l_on.get('avg_tokens', 0.0) - l_off.get('avg_tokens', 0.0)) / max(0.1, l_off.get('avg_tokens', 0.0))) * 100):+.1f}%** |
| **Correctness Rate** | {l_off.get('correctness', 0.0):.1f}% | {l_on.get('correctness', 0.0):.1f}% | **{ (l_on.get('correctness', 0.0) - l_off.get('correctness', 0.0)):+.1f}%** |
| **Avg TTFT (s)** | {l_off.get('ttft', 0.0):.3f}s | {l_on.get('ttft', 0.0):.3f}s | **{(((l_on.get('ttft', 0.0) - l_off.get('ttft', 0.0)) / max(0.001, l_off.get('ttft', 0.0))) * 100):+.1f}%** |

*Note: Results were parsed automatically from the generated JSON artifacts.*
"""
    
    # 4. Append to README.md
    if os.path.exists(README_PATH):
        with open(README_PATH, "r") as f:
            readme_content = f.read()
            
        # Ensure we don't duplicate the section if run multiple times
        if "## 5. VoIPmonitor Official Benchmark Results" in readme_content:
            readme_content = readme_content.split("## 5. VoIPmonitor Official Benchmark Results")[0]
            
        new_readme = readme_content.strip() + "\n" + table_md
        
        with open(README_PATH, "w") as f:
            f.write(new_readme)
        print("[SUCCESS] Successfully updated README.md with VoIPmonitor official benchmark results!", flush=True)

    # 5. Push everything to GitHub
    print("\n[PUSH] Committing and pushing results to GitHub...", flush=True)
    # Remove old unused JSONs from git index if they were tracked
    subprocess.run(["git", "rm", "-f", "estonia_off.json", "estonia_on.json"], capture_output=True)
    subprocess.run(["git", "add", "hotel_lights_off.json", "hotel_lights_on.json", "lavd_off.json", "lavd_on.json", "README.md"], check=True)
    subprocess.run(["git", "commit", "-m", "docs: update VoIPmonitor LAVD and HOTEL-LIGHTS benchmark results"], check=True)
    # Git will naturally use credentials stored in ~/.git-credentials
    subprocess.run(["git", "push", "origin", "main"], check=True)
    print("[SUCCESS] Published results and updated the repository on GitHub!", flush=True)

if __name__ == "__main__":
    main()
